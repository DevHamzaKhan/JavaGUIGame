public class Platform {
    int x; int y; int width; int height; int type; boolean active;

    public Platform(int x, int y, int width, int height, int type, boolean active) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
        this.type = type;
        this.active = active;
    }
}
